/*What is string interpolation and concatenation. Write definition with example.*/
/*String interpolation is the process of inserting variables or expressions into a string to create a new string. */
const name = "veer";
const age = 30;
const greeting = `Hello, my name is ${name} and I am ${age} years old.`;
console.log(greeting);

/*String concatenation is the process of joining two or more strings together to create a single string*/
const firstName = "veer";
const lastName = "chouhan";
const fullName = firstName + " " + lastName;
console.log(fullName);



